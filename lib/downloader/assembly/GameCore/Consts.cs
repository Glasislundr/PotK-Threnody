    public static string Format(string placeholder, IDictionary args = null)
    {
      if (args == null)
        return placeholder;
      if (Consts.transRegex == null)
        Consts.transRegex = new Regex("%\\(([^)]+)\\)s");
      return Consts.transRegex.Replace(placeholder, (MatchEvaluator) (match =>
      {
        string key = match.Groups[1].Value;
        if (args.Contains((object) key))
          return string.Concat(args[(object) key]);
        Debug.LogWarning((object) string.Format("NOT FOUND TRANS KEY \"{0}\": {1}", (object) key, (object) placeholder));
        return "";
      }));
    }

    public static Consts GetInstance()
    {
      if (Consts.instance == null)
        Consts.instance = new Consts();
      return Consts.instance;
    }

    public static void Update(ConstsConsts[] consts) => Consts.instance = new Consts(consts);

    private Consts()
    {
    }

    private Consts(ConstsConsts[] consts)
    {
      System.Type type = this.GetType();
      foreach (ConstsConsts constsConsts in consts)
      {
        string[] source = constsConsts.name.Split('.');
        if (source.Length == 2)
        {
          string name = ((IEnumerable<string>) source).First<string>();
          FieldInfo field = type.GetField(name);
          if (field == (FieldInfo) null)
            Debug.LogError((object) ("consts not found name=" + name));
          else if (!(field.GetValue((object) this) is Dictionary<string, string> dictionary))
          {
            Debug.LogError((object) ("consts cast Dictionary<string, string> is null. name=" + name));
          }
          else
          {
            string key = ((IEnumerable<string>) source).Last<string>();
            dictionary[key] = constsConsts.description;
          }
        }
        else
        {
          FieldInfo field = type.GetField(constsConsts.name);
          if (field == (FieldInfo) null)
            Debug.LogError((object) ("consts not found name=" + constsConsts.name));
          else
            field.SetValue((object) this, (object) constsConsts.description);
        }
      }
    }

    public string hummableSize(long requiredSize)
    {
      return requiredSize >= 1073741824L ? "約{0:F1}GB".F((object) ((double) requiredSize / 1073741824.0)) : (requiredSize >= 1048576L ? "約{0:F1}MB".F((object) ((double) requiredSize / 1048576.0)) : (requiredSize >= 1024L ? "約{0:F1}KB".F((object) ((double) requiredSize / 1024.0)) : "{0:F1}B".F((object) ((double) requiredSize / 1.0))));
    }

